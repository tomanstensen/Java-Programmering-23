public class javaBlockPrint {
    public static void main(String[] args) {
        System.out.println("    _____       ");
        System.out.println("   /     |          ");
        System.out.println("   JJJJJ |   ______   __     __  ______ ");
        System.out.println("      JJ |  /      \\ /  \\   /  |/      \\ ");
        System.out.println(" __   JJ |  aaaaaa  |\"\"  \\ /\"\"/ aaaaaa  |");
        System.out.println("/  |  JJ | /     aa | \"\"  /\"\"/  /    aa |");
        System.out.println("JJ \\__JJ |/aaaaaaaa |  \"\" \"\"/  /aaaaaaa | ");
        System.out.println("JJ    JJ/ aa     aa |   \"\"\"/   aa    aa | ");
        System.out.println(" JJJJJJ/   aaaaaaaa/     \"/     aaaaaaa/ ");
    }
    
}
